/*//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Calc Greatest Common Divisor
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

int calcGreatestCommonDivisor(int lhs, int rhs)
{
    if (rhs == 0)
    {
        return lhs;
    }
    else
    {
        return calcGreatestCommonDivisor(rhs, lhs - lhs / rhs * rhs);
    }
}


/*//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
// Main
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////*/

int main()
{
    int lhs;
    int rhs;

    lhs = input();
    rhs = input();

    output(calcGreatestCommonDivisor(lhs, rhs));
}
